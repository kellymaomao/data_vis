function plot_all_bars() {

  var margin_top = 10
  var margin = 80
  var width = 1000;
  var height = 600;

  d3.select('#bar-vis').select("svg").remove();

  var svg = d3.select('#bar-vis')
      .append('svg')
      .attr('width', width)
      .attr('height', height)
      .append('g')
      .attr('transform', 'translate(' + margin + ',' + margin_top + ')');

  width = width - margin - margin;
  height = height - margin_top - margin;

  var x_scale = d3.scaleBand()
      .domain(["Santa Clara County", "Alameda County", "San Mateo County"])
      .rangeRound([0, width])
      .padding(0.5);

  var y_scale = d3.scaleLinear()
      .domain([0, 1400000])
      .range([height, 0]);

  var colors = d3.scaleQuantize().domain([0,2]).range(['purple', 'red', 'green']);

  plot_bars('2011', '1');

  function plot_bars(year, month) {

      var y_axis = d3.axisLeft(y_scale);
      var x_axis = d3.axisBottom(x_scale);

      svg.append('g')
          .attr('class', 'x axis')
          .attr('transform', 'translate(0,' + height + ')');

      svg.append('g')
          .attr('class', 'y axis');

      var sales_data = data[year.concat('-', month)];
      var bars = svg.selectAll('.bar').data(sales_data)
      bars.exit().remove();

      var new_bars = bars
          .enter()
          .append('rect')
          .attr('class', 'bar')
          .attr('x', function(d) {
              return x_scale(d.county);
          })
          .attr('width', x_scale.bandwidth())
          .attr('y', height)
          .attr('height', 0)

      var trans_duration = d3.transition().duration(0);
      new_bars.merge(bars)
          .transition(trans_duration)
          .attr('y', function(d) {
              return y_scale(parseInt(d.value));
          })
          .attr('height', function(d) {
              return height - y_scale(parseInt(d.value))
          })
          .attr('fill', function(d) {
              if (d.county === "Santa Clara County") {
                return colors(0);
              } else if (d.county === "Alameda County") {
                return colors(1);
              } else if (d.county === "San Mateo County"){
                return colors(2);
              } else {
                throw "Not a valid county name";
              }
          })

      var tip = d3.tip()
        .attr('class', 'd3-tip')
        .html(function(d) {
          return "<font size='5'>" + "".concat("$ ", d.value) + "</font>";
        });

      svg.call(tip);
      svg.selectAll(".bar")
        .on('mouseover', tip.show)
        .on('mouseout', tip.hide)

      svg.select('.x.axis')
          .call(x_axis);

      svg.select('.y.axis')
          .transition(trans_duration)
          .call(y_axis);

      svg.append("text")
          .attr("text-anchor", "middle")
          .attr("transform", "translate("+ (margin-145) +","+(margin+140)+")rotate(-90)")
          .text("Median House Sale Price ($)");
  }

  var slider_year = d3.select('#year');
  var slider_month = d3.select('#month');

  slider_year.on('change', function() {
      plot_bars(this.value, document.getElementById('month').value);
  });
  slider_month.on('change', function() {
      plot_bars(document.getElementById('year').value, this.value);
  });
}
